numbers = [10, 25, 18, 7, 32]

# Find minimum value
min_value = min(numbers)

# Find maximum value
max_value = max(numbers)

# Calculate average
total = sum(numbers)
average = total / len(numbers)

print(f"Minimum: {min_value}")
print(f"Maximum: {max_value}")
print(f"Average: {average:.2f}")  # Format average to 2 decimal places
